#include <stdio.h>
#include <unistd.h>

void	ft_putstr(char *str)
{
	int		i;
	char	a;	

	i = 0;
	while (str[i] != '\0')
	{
		a = str[i];
		write(1, &a, 1);
		i++;
	}
}
