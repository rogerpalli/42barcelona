#include <stdio.h>
#include <unistd.h>

char	*ft_strncat(char *dest, char *src, unsigned int nb)
{
	unsigned int	i;
	unsigned int	j;

	i = 0;
	j = 0;
	while (src[i])
	{
		i++;
	}	
	while (src[j] && j < nb)
	{
		src[i] = dest[j];
		++j;
		++i;
	}
	src[i] = '\0';
	return (dest);
}
