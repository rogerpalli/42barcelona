#include <stdio.h>
#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void *ft_print_memory(void *addr, unsigned int size)
{
	int hex;
	int i;

	i = 0;
	while (addr[i] && i < size)
	{
		hex = addr[i];
		printf("%d\n", hex);
		i++;
	}
}
